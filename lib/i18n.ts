export function getText(lang: Lang, key: string): string {
  const keys = key.split('.')
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  let val: any = t[lang]
  for (const k of keys) val = val?.[k]
  return typeof val === 'string' ? val : key
}
